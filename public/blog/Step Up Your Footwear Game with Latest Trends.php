<div class="mx-6 md:mx-28 text-lg md:text-2xl mt-6 italic">
    02. Effortless Linen-Inspired Footwear
</div>

<div class="mx-6 md:mx-28 mt-2 text-justify text-sm md:text-base">
    Inspired by the lightweight and breathable nature of linen, our summer footwear collection offers unparalleled comfort and timeless style. These shoes are perfect for sunny days, ensuring your feet stay cool and stylish no matter the occasion.
    <span class="font-thin"><br>(Style Tip: Pair linen-inspired loafers or sandals with breezy summer outfits for a relaxed yet polished vibe.)</span>
</div>

<div class="mx-6 md:mx-28 text-lg md:text-2xl mt-6 italic">
    03. Sustainable Soles
</div>

<div class="mx-6 md:mx-28 mt-2 text-justify text-sm md:text-base">
