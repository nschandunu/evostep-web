    04. Athleisure Evolution
</div>

<div class="mx-6 md:mx-28 mt-2 text-justify text-sm md:text-base">
    Athleisure-inspired footwear is dominating the trends. From sleek trainers to stylish slip-ons, our collection combines the perfect balance of comfort and style, ensuring a seamless transition from workouts to casual outings.
    <span class="font-thin"><br>(Style Tip: Pair trendy trainers with joggers or leggings for a sporty yet stylish look. Add a matching hoodie to complete the vibe.)</span>
</div>

<div class="mx-6 md:mx-28 text-lg md:text-2xl mt-6 italic">
    05. Minimalist Chic
</div>

<div class="mx-6 md:mx-28 mt-2 text-justify text-sm md:text-base">
